package Ex_01;


public class Calculadora {
    
    static void soma(double a, double b){
        double soma = a + b;
        System.out.println("A soma dos algoritimos é: " + soma);
    }

    static void subtracao(double a, double b){
        double subtracao = a - b;
        System.out.println("A subtração dos algoritimos é: " + subtracao);
    }

    static void multiplicacao(double a, double b){
        double multiplicacao = a * b;
        System.out.println("A multiplicação dos algoritimos é: " + multiplicacao);
    }

    static void divisao(double a, double b){
        double divisao = a / b;
        System.out.println("A divisão dos algoritimos é: " + divisao);
    }
}
